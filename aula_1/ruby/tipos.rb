#Variáveis são referencias a espaços de memória na máquina que guardam valores
#Variável tem: Nome, Valor e Tipo
#Os tipos das variáveis são definidos em tempo de atribuição
# o símbolo "=" diz respeito a ação de atribuição de valor

nome = "Mac book"
#Nome: nome
#Valor: Mac book
#Tipo: String

quantidade = 2
#Nome: quantidade
#Valor: 2
#Tipo: Integer

preco = 3000.50
#Nome: preco
#Valor: 3000.50
#Tipo: Float

disponivel = false #true
#Nome: disponivel
#Valor: true
#Tipo: TrueClass / FalseClass (Boolean)

puts nome
puts quantidade
puts preco
puts disponivel
